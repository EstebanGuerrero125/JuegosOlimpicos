package modelo;

import java.util.*;


/**
 *
 * @author user
 */
public class Evento {
    int Id;
    String nombre;
    String lugar;
    String fecha;
    

    public Evento() {
    }

    public Evento(int Id, String nombre, String lugar, String fecha) {
        this.Id = Id;
        this.nombre = nombre;
        this.lugar = lugar;
        this.fecha = fecha;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }

   
    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
}
